package Conditional_Statements;

public class Switch_Case {

	public static void main(String[] args) {
		int Percentage = 75 ;
		switch(Percentage) {
		case 90:{
			System.out.println("I will pursue Science.");
			break;
		}
		case 70:{
			System.out.println("I will pursue Commerce.");
			break;
		}
		case 60:{
			System.out.println("I will pursue Diploma.");
			break;
		}
		case 50:{
			System.out.println("I will pursue Arts.");
			break;
		}
		
		}

	}

	}

